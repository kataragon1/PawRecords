// â”€â”€ DETECT BULK SUMMARY â”€â”€
export async function detectBulkSummary(textSnippet) {
  const hasDateRange = /(\d{1,2}\/\d{1,2}\/\d{4}|\d{4}-\d{2}-\d{2})\s*[-â€“â€”to]+\s*(\d{1,2}\/\d{1,2}\/\d{4}|\d{4}-\d{2}-\d{2})/i.test(textSnippet);
  const dateMatches = (textSnippet.match(/\d{4}-\d{2}-\d{2}|\d{1,2}\/\d{1,2}\/\d{2,4}/g) || []);
  const hasMultipleDates = dateMatches.length > 3;
  const hasProblemList = /problem list|active diagnoses|current medications|visit history|medical history|patient history/i.test(textSnippet);
  const hasPortalExport = /medical record|visit summary|discharge summary.*\n.*discharge summary/i.test(textSnippet);
  return hasDateRange || (hasMultipleDates && (hasProblemList || hasPortalExport));
}

// â”€â”€ SPLIT BY DATE â”€â”€
export async function splitByDate(fullText) {
  const datePattern = /(?:^|\n)(?:Date of Service|Service Date|Visit Date|DOS|Date|Appointment Date|Exam Date|Discharge Date):\s*(\d{1,2}\/\d{1,2}\/\d{2,4}|\d{4}-\d{2}-\d{2})/gi;
  const standaloneDatePattern = /(?:^|\n)(\d{1,2}\/\d{1,2}\/\d{4})\s*\n/g;

  const splitPoints = new Set();
  let match;
  while ((match = datePattern.exec(fullText)) !== null) {
    if (match.index > 100) splitPoints.add(match.index);
  }
  while ((match = standaloneDatePattern.exec(fullText)) !== null) {
    if (match.index > 100) splitPoints.add(match.index);
  }

  const sortedPoints = [...splitPoints].sort((a, b) => a - b);
  if (sortedPoints.length < 2) return [fullText];

  const splits = [];
  let lastIdx = 0;
  for (const pt of sortedPoints) {
    if (pt > lastIdx + 100) {
      splits.push(fullText.slice(lastIdx, pt));
      lastIdx = pt;
    }
  }
  splits.push(fullText.slice(lastIdx));
  return splits.filter(s => s.trim().length > 100);
}

// â”€â”€ LAB GROUP NORMALIZER â”€â”€
const BP_TESTS = new Set([
  'systolic blood pressure','diastolic blood pressure','mean arterial pressure',
  'blood pressure','systolic','diastolic'
]);

export function normalizeLabGroups(labs) {
  for (const lab of labs) {
    const t = (lab.test || '').toLowerCase().trim();
    if (_labCorrections[t]) { lab.labGroup = _labCorrections[t]; continue; }
    if (BP_TESTS.has(t)) { lab._isBP = true; continue; }
    if (lab.labGroup) continue;
    if (lab.labName) { lab.labGroup = lab.labName; continue; }
    lab.labGroup = 'Other';
  }
}

export function stripBPFromLabs(labs) {
  return labs.filter(l => !l._isBP);
}

