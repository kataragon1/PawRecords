// â”€â”€ PROCESS FILE â”€â”€
export async function processFile(file) {
  const ext = file.name.split('.').pop().toLowerCase();
  const mime = file.mimeType || '';

  // Check for cached extraction
  try {
    const cached = await getDoc(doc(db, 'extractions', file.id));
    if (cached.exists() && cached.data().result && !cached.data().committed) {
      const data = JSON.parse(cached.data().result);
      if (data.allVisits || data.allLabs) {
        showToast(`${file.name.slice(0,25)}: using cached extraction â€” no API cost`, 'journal');
        const allCats = new Set(data.cats || []);
        const allDates = new Set(data.dates || []);
        await writeExtractionToFirestore(file, data.allVisits||[], data.allLabs||[], data.allFlags||[], allCats, allDates, data.passes||[]);
        return;
      }
    }
  } catch(e) { /* no cache, proceed normally */ }

  await writeFileStatus(file.id, file.name, 'processing');

  // SHA-256 deduplication
  let fileHash = null;
  try {
    const blobForHash = await fetchDriveFileBlob(file.id);
    const buf = await blobForHash.arrayBuffer();
    const hashBuf = await crypto.subtle.digest('SHA-256', buf);
    fileHash = Array.from(new Uint8Array(hashBuf)).map(b => b.toString(16).padStart(2,'0')).join('');

    const dupSnap = await getDocs(query(
      collection(db, 'files'),
      where('fileHash', '==', fileHash),
      where('status', '==', 'complete')
    ));
    if (!dupSnap.empty && !dupSnap.docs.some(d => (d.data().driveFileId || d.id) === file.id)) {
      const dupDoc = dupSnap.docs[0];
      const dupFileId = dupDoc.data().driveFileId || dupDoc.id;
      const [vSnap, lSnap] = await Promise.all([
        getDocs(query(collection(db, 'visits'), where('driveFileIds', 'array-contains', dupFileId))),
        getDocs(query(collection(db, 'labs'), where('driveFileId', '==', dupFileId)))
      ]);
      const hasRecords = vSnap.size > 0 || lSnap.size > 0;
      if (hasRecords) {
        const dupName = dupDoc.data().filename || 'another file';
        showAlert(`${file.name.slice(0,40)}: duplicate of "${dupName.slice(0,30)}" (${vSnap.size} visits, ${lSnap.size} labs) â€” skipped`);
        await writeFileStatus(file.id, file.name, 'complete', {
          fileHash, extractionPasses: ['exact_duplicate'], duplicateOf: dupDoc.id
        });
        return;
      }
      console.log(`Duplicate hash found (${dupDoc.data().filename}) but has no records â€” processing anyway`);
    }
  } catch(e) { console.warn('Hash check failed (non-critical):', e.message); }

  let extractedText = null;
  let isImage = false;
  let imageBase64 = null;
  let imageMediaType = null;

  if (ext === 'xlsx' || ext === 'xls' || mime.includes('spreadsheet')) {
    await processExcelFile(file);
    return;
  } else if (['jpg','jpeg','png','gif','webp','tiff','tif'].includes(ext)) {
    isImage = true;
    const blob = await fetchDriveFileBlob(file.id);
    imageBase64 = await blobToBase64(blob);
    imageMediaType = blob.type || `image/${ext === 'jpg' ? 'jpeg' : ext}`;
  } else if (ext === 'pdf' || mime === 'application/pdf') {
    $('progress-current').textContent = `${file.name}: extracting textâ€¦`;
    const blob = await fetchDriveFileBlob(file.id);
    extractedText = await extractPdfText(blob);
    if (!extractedText || extractedText.trim().length < 50) {
      $('progress-current').textContent = `${file.name}: rendering as imageâ€¦`;
      imageBase64 = await pdfPageToBase64(blob);
      imageMediaType = 'image/png';
      isImage = true;
      extractedText = null;
    }
  } else if (['txt','md','csv'].includes(ext)) {
    const blob = await fetchDriveFileBlob(file.id);
    extractedText = await blob.text();
  } else {
    await writeFileStatus(file.id, file.name, 'complete', {
      flags: [{ type: 'unknown_type', description: `Unsupported file type: ${ext}` }]
    });
    return;
  }

  // Split into chunks
  let chunks = [{ text: extractedText, imageBase64, imageMediaType, isImage }];
  if (extractedText) {
    const CHUNK_SIZE = 15000;
    if (extractedText.length > CHUNK_SIZE) {
      const isBulkSummary = await detectBulkSummary(extractedText.slice(0, 2000));
      let splitTexts = isBulkSummary ? await splitByDate(extractedText) : [extractedText];
      const finalChunks = [];
      for (const t of splitTexts) {
        if (t.length <= CHUNK_SIZE) {
          finalChunks.push(t);
        } else {
          let pos = 0;
          while (pos < t.length) {
            let end = Math.min(pos + CHUNK_SIZE, t.length);
            if (end < t.length) {
              const breakAt = t.lastIndexOf('\n\n', end);
              if (breakAt > pos + 5000) end = breakAt;
            }
            finalChunks.push(t.slice(pos, end));
            pos = end;
          }
        }
      }
      if (finalChunks.length > 1) {
        chunks = finalChunks.map((t, i) => ({ text: t, isImage: false, _chunkIndex: i, _totalChunks: finalChunks.length, _isBulkHistory: isBulkSummary }));
        $('progress-current').textContent = `${file.name}: split into ${chunks.length} chunks`;
      }
    }
  }

  const allVisits = [];
  const allLabs = [];
  const allFlags = [];
  const allCats = new Set();
  const allDates = new Set();
  const passes = [];
  let isAmendment = false;
  let chunkContext = null;

  for (let ci = 0; ci < chunks.length; ci++) {
    const chunk = chunks[ci];
    if (processingStop) break;
    if (chunks.length > 1) {
      $('progress-current').textContent = `${file.name}: chunk ${ci+1}/${chunks.length}`;
      $('progress-label').textContent = `Processingâ€¦ (chunk ${ci+1}/${chunks.length})`;
    }

    if (ci > 0 && chunkContext) {
      chunk._context = chunkContext;
      chunk._isContinuation = true;
      chunk._chunkIndex = chunk._chunkIndex ?? ci;
      chunk._totalChunks = chunk._totalChunks ?? chunks.length;
      chunk._isBulkHistory = chunk._isBulkHistory ?? chunks[0]?._isBulkHistory ?? false;
    }

    const result = await runCombinedPass(file, chunk);
    if (result === 'SKIP') {
      console.log(`${file.name}: chunk skipped (no clinical content)`);
      continue;
    }
    if (!result) {
      if (chunk._retried) {
        const chunkSize = chunk.text?.length || 0;
        const estimatedCost = ((chunkSize / 4) / 1000000 * 3).toFixed(4);
        const choice = await promptChunkFailure(file.name, chunkSize, estimatedCost);
        if (choice === 'split') {
          const mid = chunk.text.lastIndexOf('\n\n', Math.floor(chunkSize / 2)) || Math.floor(chunkSize / 2);
          chunks.splice(ci + 1, 0,
            { text: chunk.text.slice(0, mid), isImage: false, _retried: false },
            { text: chunk.text.slice(mid), isImage: false, _retried: false }
          );
        } else if (choice === 'skip') {
          allFlags.push({ type: 'chunk_skipped', description: `User skipped chunk of ${chunkSize} chars`, bestGuess: null });
        } else {
          processingStop = true;
        }
        continue;
      }
      console.warn(`${file.name}: chunk failed â€” retrying once`);
      chunks.splice(ci + 1, 0, { ...chunk, _retried: true });
      continue;
    }
    if (result.skip) {
      console.log(`${file.name}: skipped (invoice/no clinical content)`);
      continue;
    }

    console.log(`${file.name}: extracted visits=${result.visits?.length||0} labs=${result.labs?.length||0} pcr=${result.pcr?.length||0} flags=${result.flags?.length||0}`);
    if (!result.labs?.length && (chunk.text||'').toLowerCase().match(/\b(rbc|hematocrit|hematology|creatinine|albumin|platelets|lymphocytes|chemistry)\b/)) {
      console.warn(`${file.name}: labs expected but none extracted â€” chunk text (first 3000 chars):`, (chunk.text||'').slice(0,3000));
    }

    (result.cats || []).forEach(c => allCats.add(c));
    (result.dates || []).forEach(d => allDates.add(d));
    if (result.visits?.length) { allVisits.push(...result.visits); passes.push('narrative'); }
    if (result.labs?.length) { allLabs.push(...result.labs); passes.push('labs'); }
    if (result.pcr?.length) { allLabs.push(...result.pcr.map(r => ({ ...r, labName: r.labName || 'PCR', labGroup: r.labGroup || 'PCR' }))); passes.push('pcr'); }
    if (result.flags?.length) allFlags.push(...result.flags);
    if (result.isAmendment) isAmendment = true;

    if (!chunkContext) {
      const v0 = result.visits?.[0];
      const cat0 = v0?.cat || result.cats?.[0] || null;
      const date0 = v0?.date || result.dates?.[0] || null;
      if (cat0 || date0) {
        chunkContext = { cat: cat0, date: date0, clinic: v0?.clinic || null, doctor: v0?.doctor || null };
      }
    } else {
      const newCat = result.visits?.[0]?.cat || result.cats?.[0];
      const newClinic = result.visits?.[0]?.clinic;
      if (newCat && !chunkContext.cat) chunkContext.cat = newCat;
      if (newClinic && !chunkContext.clinic) chunkContext.clinic = newClinic;
    }
  }

  // Reset chunk label
  if (chunks.length > 1) $('progress-label').textContent = 'Processingâ€¦';

  // Pre-write checks
  {
    const issues = [];
    const qualityFlags = [];

    const knownCatSet = new Set((APP_PETS || []).map(p => p.toLowerCase()));
    const extractedCats = [...allCats].filter(Boolean);
    const unknownCats = extractedCats.filter(c => knownCatSet.size > 0 && !knownCatSet.has(c.toLowerCase()));

    if (!extractedCats.length) {
      issues.push({ type: 'cat', label: 'Which cat is this record for?', current: null });
    } else if (knownCatSet.size > 0 && unknownCats.length) {
      issues.push({ type: 'cat', label: `Cat "${unknownCats[0]}" not in known list â€” confirm or correct:`, current: unknownCats[0] });
    }

    const extractedDates = allVisits.map(v => v.date).filter(d => d && d !== 'unknown');
    if (!extractedDates.length && allVisits.length > 0) {
      issues.push({ type: 'date', label: 'What is the visit date?', current: null });
    }

    const lowConfVisits = allVisits.filter(v => (v.confidence || 0.8) < 0.6);
    if (lowConfVisits.length) {
      issues.push({
        type: 'confirm',
        label: `Claude has low confidence in this extraction (${Math.round((lowConfVisits[0].confidence||0)*100)}%). Review before importing?`,
        detail: lowConfVisits[0].synopsis || lowConfVisits[0].chiefComplaint || 'No summary available'
      });
    }

    if (extractedCats.length > 1 && knownCatSet.size > 0) {
      issues.push({
        type: 'multicat',
        label: `Multiple cats found in this file: ${extractedCats.join(', ')}. Is this correct?`,
        cats: extractedCats
      });
    }

    // Duplicate/amendment detection
    if (_allVisitsCache && allVisits.length > 0) {
      for (const v of allVisits) {
        const clinicMatch = _allVisitsCache.find(e =>
          e.cat?.toLowerCase() === v.cat?.toLowerCase() &&
          e.date === v.date &&
          (!e.clinic || !v.clinic || e.clinic?.toLowerCase() === v.clinic?.toLowerCase())
        );
        if (clinicMatch) {
          const simScore = textSimilarity(v.narrative || v.synopsis || '', clinicMatch.narrative || clinicMatch.synopsis || '');
          if (simScore > 0.2) {
            v._mergeInto = clinicMatch.id;
            isAmendment = true;
          }
        }
      }

      const mergeCount = allVisits.filter(v => v._mergeInto).length;
      if (mergeCount > 0 && mergeCount === allVisits.length && allLabs.length === 0) {
        await writeFileStatus(file.id, file.name, 'complete', {
          extractionPasses: ['merged_into_existing'],
          fileHash: fileHash || null
        });
        updateFileBadge(file.id, 'complete');
        showToast(`${file.name.slice(0,30)}: merged into existing record âś“`, 'journal');
        return;
      }
    }

    // Quality flags
    const shortSynopsis = allVisits.filter(v => !v._skipDuplicate && (!v.synopsis || v.synopsis.length < 30));
    if (shortSynopsis.length) {
      qualityFlags.push({ type: 'quality_warning', description: `Synopsis missing or very short â€” extraction may be incomplete.`, bestGuess: null });
    }
    const shortNarrative = allVisits.filter(v => !v._skipDuplicate && v.narrative && v.narrative.length < 150);
    if (shortNarrative.length) {
      qualityFlags.push({ type: 'quality_warning', description: `Narrative unusually short (${shortNarrative[0].narrative?.length} chars) â€” clinical detail may be missing.`, bestGuess: null });
    }
    const textHasLabKeywords = (extractedText || '').toLowerCase().match(/\b(cbc|chemistry|hematology|urinalysis|creatinine|albumin|platelets|lymphocytes)\b/);
    if (textHasLabKeywords && allLabs.length === 0) {
      qualityFlags.push({ type: 'quality_warning', description: 'Document appears to contain lab results but none were extracted â€” may need reprocessing.', bestGuess: null });
    }
    if (isAmendment && _allVisitsCache) {
      const hasMatchingVisit = allVisits.some(v => _allVisitsCache.some(e => e.cat === v.cat && e.date === v.date));
      if (!hasMatchingVisit) {
        qualityFlags.push({ type: 'quality_warning', description: 'Document flagged as amendment but no existing record found â€” importing as new record.', bestGuess: null });
        isAmendment = false;
      }
    }

    allFlags.push(...qualityFlags);

    if (issues.length && (allVisits.length > 0 || allLabs.length > 0)) {
      const corrections = await promptClarification(file.name, issues, file.id);
      if (corrections === null) {
        await writeFileStatus(file.id, file.name, 'unprocessed');
        updateFileBadge(file.id, 'unprocessed');
        return 'skipped';
      }
      if (corrections.cat) {
        for (const v of allVisits) v.cat = corrections.cat;
        for (const l of allLabs) l.cat = corrections.cat;
        allCats.clear(); allCats.add(corrections.cat);
      }
      if (corrections.date) {
        for (const v of allVisits) if (!v.date || v.date === 'unknown') v.date = corrections.date;
        for (const l of allLabs) if (!l.resultDate) l.resultDate = corrections.date;
        allDates.add(corrections.date);
      }
    }
  }

  // Normalize labs
  normalizeLabGroups(allLabs);
  allLabs.splice(0, allLabs.length, ...stripBPFromLabs(allLabs));

  // Reconcile lab cat names from chunk context
  if (chunkContext?.cat) {
    const knownCats = new Set((APP_PETS || []).map(p => p.toLowerCase()));
    const badCats = new Set();
    for (const lab of allLabs) {
      if (!lab.cat || (knownCats.size > 0 && !knownCats.has((lab.cat || '').toLowerCase()))) {
        badCats.add(lab.cat || '(blank)');
        lab.cat = chunkContext.cat;
      }
    }
    if (badCats.size > 0) {
      allFlags.push({
        type: 'no_cats',
        description: `Labs had unrecognized cat name(s): ${[...badCats].join(', ')}. Reassigned to ${chunkContext.cat} â€” please verify.`,
        bestGuess: chunkContext.cat
      });
    }
    allCats.add(chunkContext.cat);
  }

  if (processingStop && !allVisits.length && !allLabs.length) {
    await writeFileStatus(file.id, file.name, 'unprocessed');
    return 'skipped';
  }

  await writeExtractionToFirestore(file, allVisits, allLabs, allFlags, allCats, allDates, passes, isAmendment, fileHash);
}

// â”€â”€ WRITE EXTRACTION TO FIRESTORE â”€â”€
export async function writeExtractionToFirestore(file, allVisits, allLabs, allFlags, allCats, allDates, passes, isAmendment = false, fileHash = null) {
  // Save raw extraction result to Firestore immediately
  const extractionCache = { allVisits, allLabs, allFlags, cats: [...allCats], dates: [...allDates], passes: [...new Set(passes)] };
  try {
    await setDoc(doc(db, 'extractions', file.id), {
      fileId: file.id, filename: file.name,
      result: JSON.stringify(extractionCache).slice(0, 900000),
      createdAt: new Date().toISOString()
    });
  } catch(e) { console.warn('Extraction cache write failed (non-critical):', e.message); }

  // Clear stale flags
  try {
    const oldFlags = await getDocs(query(collection(db, 'flags'), where('fileId', '==', file.id)));
    for (const d of oldFlags.docs) await deleteDoc(d.ref);
  } catch(e) { console.warn('Flag clear failed (non-critical):', e.message); }

  const visitDates = allVisits.map(v => v.date).filter(Boolean);
  const allKnownDates = [...new Set([...allDates, ...visitDates])].sort().reverse();
  const primaryDate = allKnownDates[0] || null;

  // Pre-fetch existing visits for amendment handling
  const amendmentMatches = {};
  if (isAmendment) {
    for (const visit of allVisits) {
      if (!visit.cat || visit.cat === 'SKIP') continue;
      try {
        if (visit._mergeInto) {
          const existDoc = await getDoc(doc(db, 'visits', visit._mergeInto));
          if (existDoc.exists()) { amendmentMatches[`${visit.cat}_${visit.date}`] = existDoc; continue; }
        }
        const existingSnap = await getDocs(query(
          collection(db, 'visits'),
          where('cat', '==', visit.cat),
          where('date', '==', visit.date || 'unknown')
        ));
        if (!existingSnap.empty) { amendmentMatches[`${visit.cat}_${visit.date}`] = existingSnap.docs[0]; }
      } catch(e) { /* non-critical */ }
    }
  }

  try {
    let fsBatch = writeBatch(db);
    let fsBatchCount = 0;
    const fsFlush = async () => {
      if (fsBatchCount > 0 && fsBatchCount % 400 === 0) {
        await fsBatch.commit();
        fsBatch = writeBatch(db);
      }
    };

    for (const visit of allVisits) {
      if (!visit.cat || visit.cat === 'SKIP') continue;

      const matchKey = `${visit.cat}_${visit.date}`;
      if (isAmendment && amendmentMatches[matchKey]) {
        const existDoc = amendmentMatches[matchKey];
        const existData = existDoc.data();
        const merged = {
          synopsis: existData.synopsis || visit.synopsis,
          narrative: existData.narrative?.length > (visit.narrative?.length || 0) ? existData.narrative : (visit.narrative || existData.narrative),
          chiefComplaint: existData.chiefComplaint || visit.chiefComplaint,
          visitClass: existData.visitClass || visit.visitClass || null,
          medications: existData.medications?.length ? existData.medications : (visit.medications || []),
          procedures: existData.procedures?.length ? existData.procedures : (visit.procedures || []),
          vitals: existData.vitals || visit.vitals,
          doctor: existData.doctor || visit.doctor,
          docType: existData.docType || visit.docType,
          updatedAt: new Date().toISOString(),
          sourceFiles: [...new Set([...(existData.sourceFiles||[]), file.name])],
          driveFileIds: [...new Set([...(existData.driveFileIds||[]), file.id])]
        };
        Object.keys(merged).forEach(k => merged[k] === null && delete merged[k]);
        fsBatch.update(existDoc.ref, sanitizeForFirestore(merged));
        if (visit.cat) allCats.add(visit.cat);
        if (visit.date) allDates.add(visit.date);
        fsBatchCount++; await fsFlush();
        continue;
      }

      fsBatch.set(doc(collection(db, 'visits')), sanitizeForFirestore({
        cat: visit.cat, date: visit.date || 'unknown',
        clinic: visit.clinic || null, doctor: visit.doctor || null,
        chiefComplaint: visit.chiefComplaint || null,
        visitClass: visit.visitClass || null,
        synopsis: visit.synopsis || null,
        docType: visit.docType || null,
        medications: visit.medications || [],
        procedures: visit.procedures || [], vitals: visit.vitals || null,
        narrative: visit.narrative || '',
        sourceFiles: [file.name], driveFileIds: [file.id], sourceFileId: file.id,
        visitCluster: `${visit.date||'unknown'}_${(visit.clinic||'unknown').toLowerCase().replace(/\s+/g,'_').slice(0,20)}`,
        confidence: visit.confidence || 0.8, flags: visit.flags || [],
        createdAt: new Date().toISOString()
      }));
      if (visit.cat) allCats.add(visit.cat);
      if (visit.date) allDates.add(visit.date);
      fsBatchCount++; await fsFlush();
    }

    for (const lab of allLabs) {
      if (!lab.test || lab.value === undefined || lab.value === null) continue;
      const cat = lab.cat || (allCats.size === 1 ? [...allCats][0] : null);
      if (!cat) continue;
      const resolvedDate = lab.resultDate || lab.visitDate || primaryDate;

      // Dedup: skip if an identical result already exists in Firestore,
      // unless the stored value is a string and the new one is numeric (upgrade).
      if (_allLabsCache) {
        const existing = _allLabsCache.find(l =>
          l.cat === cat && l.test === lab.test &&
          (l.resultDate === resolvedDate || l.visitDate === resolvedDate)
        );
        if (existing) {
          const newIsNumeric = typeof lab.value === 'number';
          const existingIsString = typeof existing.value === 'string';
          // Only allow write if we're upgrading a qualitative string to a numeric value
          if (!(newIsNumeric && existingIsString)) continue;
        }
      }

      fsBatch.set(doc(collection(db, 'labs')), sanitizeForFirestore({
        cat, test: lab.test, value: lab.value, unit: lab.unit || null,
        refLow: lab.refLow ?? null, refHigh: lab.refHigh ?? null, abnormal: lab.abnormal || null,
        resultDate: resolvedDate, visitDate: resolvedDate,
        labName: lab.labName || null, labGroup: lab.labGroup || null,
        source: lab.source || null,
        sourceFile: file.name, driveFileId: file.id,
        createdAt: new Date().toISOString()
      }));
      fsBatchCount++; await fsFlush();
    }

    for (const flag of allFlags) {
      fsBatch.set(doc(collection(db, 'flags')), sanitizeForFirestore({
        fileId: file.id, filename: file.name, type: flag.type || 'unclear_structure',
        description: flag.description || '', bestGuess: flag.bestGuess || null,
        options: ['Accept guess', 'Enter manually', 'View original'],
        resolved: false, resolvedAt: null, resolution: null,
        createdAt: new Date().toISOString()
      }));
      fsBatchCount++; await fsFlush();
    }

    await fsBatch.commit();

    // Invalidate labs cache via lazy import
    import('./labs.js').then(m => m.invalidateLabsCache());

    // Success â€” clean up extraction cache
    try { await setDoc(doc(db, 'extractions', file.id), { committed: true }, { merge: true }); } catch(e) {}

  } catch(batchErr) {
    console.error('Batch write failed:', batchErr.message);
    await writeFileStatus(file.id, file.name, 'failed', {
      error: 'Write failed â€” extraction cached. Click reprocess to retry write only.',
      extractionCached: true
    });
    showAlert(`Write failed for ${file.name.slice(0,25)} â€” extraction saved. Click reprocess to retry.`, 'warning');
    return;
  }

  // Final file status
  const ACTIONABLE_FLAG_TYPES = ['no_date', 'no_cats', 'unknown_type', 'chunk_skipped'];
  const actionableFlags = allFlags.filter(f => ACTIONABLE_FLAG_TYPES.includes(f.type));
  const hasData = allVisits.length > 0 || allLabs.length > 0;
  const finalStatus = (!hasData && allFlags.length === 0) ? 'empty'
    : actionableFlags.length > 0 ? 'flagged'
    : 'complete';
  await writeFileStatus(file.id, file.name, finalStatus, {
    cats: [...allCats],
    datesCovered: [...allDates].sort(),
    extractionPasses: [...new Set(passes)],
    flags: allFlags.map(f => f.type),
    fileHash: fileHash || null,
    processedAt: new Date().toISOString()
  });

  const catsEl = $(`cats-${file.id}`);
  if (catsEl && allCats.size) catsEl.textContent = [...allCats].join(', ');

  const ACTIONABLE_FLAG_TYPES2 = ['no_date', 'no_cats', 'unknown_type', 'chunk_skipped'];
  const newActionableCount = allFlags.filter(f => ACTIONABLE_FLAG_TYPES2.includes(f.type)).length;
  const flagCount = parseInt($('flags-count').textContent || '0') + newActionableCount;
  $('flags-count').textContent = flagCount;
  if (flagCount > 0) $('flags-btn').style.display = 'inline-flex';
}
