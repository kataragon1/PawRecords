// â”€â”€ FETCH DRIVE FILE â”€â”€
export async function fetchDriveFileBlob(fileId) {
  const res = await fetch(
    `https://www.googleapis.com/drive/v3/files/${fileId}?alt=media`,
    { headers: { Authorization: `Bearer ${driveAccessToken}` } }
  );
  if (res.status === 401) {
    setDriveAccessToken(null);
    try { sessionStorage.removeItem('pr_drive_token'); } catch(e) {}
    // Signal the queue to stop
    const filesMod = await import('./files.js');
    filesMod.setProcessingStop?.(true);
    $('drive-dot').className = 'status-dot amber';
    $('drive-status').textContent = 'Drive token expired';
    filesMod.showDriveConnectPrompt('Drive access token expired during processing. Reconnect and re-run to continue.');
    throw new Error('Drive token expired (401) â€” reconnect Drive and retry');
  }
  if (!res.ok) throw new Error(`Drive fetch failed: ${res.status}`);
  return res.blob();
}

export function blobToBase64(blob) {
  return new Promise((resolve, reject) => {
    const reader = new FileReader();
    reader.onload = () => resolve(reader.result.split(',')[1]);
    reader.onerror = reject;
    reader.readAsDataURL(blob);
  });
}

// â”€â”€ PDF.js â”€â”€
export async function getPdfJs() {
  if (pdfjs) return pdfjs;
  const script = document.createElement('script');
  script.src = 'https://cdnjs.cloudflare.com/ajax/libs/pdf.js/3.11.174/pdf.min.js';
  document.head.appendChild(script);
  await new Promise((r, j) => { script.onload = r; script.onerror = j; });
  window.pdfjsLib.GlobalWorkerOptions.workerSrc =
    'https://cdnjs.cloudflare.com/ajax/libs/pdf.js/3.11.174/pdf.worker.min.js';
  setPdfjs(window.pdfjsLib);
  return window.pdfjsLib;
}

export async function extractPdfText(blob) {
  try {
    const lib = await getPdfJs();
    const arrayBuffer = await blob.arrayBuffer();
    const pdf = await lib.getDocument({ data: arrayBuffer }).promise;
    let text = '';
    const maxPages = pdf.numPages;
    for (let i = 1; i <= maxPages; i++) {
      const page = await pdf.getPage(i);
      const content = await page.getTextContent();
      const lines = [];
      for (const item of content.items) {
        if (!item.str.trim()) continue;
        const x = item.transform[4];
        const y = item.transform[5];
        const existing = lines.find(l => Math.abs(l.y - y) < 3);
        if (existing) {
          existing.items.push({ x, str: item.str });
        } else {
          lines.push({ y, items: [{ x, str: item.str }] });
        }
      }
      lines.sort((a, b) => b.y - a.y);
      const pageText = lines.map(line => {
        line.items.sort((a, b) => a.x - b.x);
        return line.items.map(it => it.str).join('  ');
      }).join('\n');
      text += pageText + '\n';
    }
    return stripBoilerplate(text.trim());
  } catch (err) {
    console.warn('PDF.js extraction failed:', err);
    return '';
  }
}

export function stripBoilerplate(text) {
  if (!text) return text;
  return text
    .replace(/\n{3,}/g, '\n\n')
    .replace(/^.*(thank you for choosing|thank you for visiting|this document is confidential|for questions.*please call|please call.*to schedule|if you have.*questions.*contact|portal\..*\.com|www\..*\.com|fax:?\s*\d[\d\s\-()]+|powered by|generated by|printed (on|for)|page \d+ of \d+|confidential.*do not distribute).*$/gim, '')
    .replace(/your next appointment.*?\n/gi, '')
    .replace(/reminder:.*?\n/gi, '')
    .replace(/\n{3,}/g, '\n\n')
    .trim();
}

export async function pdfPageToBase64(blob) {
  try {
    const lib = await getPdfJs();
    const arrayBuffer = await blob.arrayBuffer();
    const pdf = await lib.getDocument({ data: arrayBuffer }).promise;
    const page = await pdf.getPage(1);
    const viewport = page.getViewport({ scale: 1.5 });
    const canvas = document.createElement('canvas');
    canvas.width = viewport.width;
    canvas.height = viewport.height;
    await page.render({ canvasContext: canvas.getContext('2d'), viewport }).promise;
    return canvas.toDataURL('image/png').split(',')[1];
  } catch (err) {
    console.warn('PDF page render failed:', err);
    return null;
  }
}

