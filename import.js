// â”€â”€ CLAUDE EXTRACTION API CALL â”€â”€
export async function callExtractionClaude(messages, maxTokens = 2000) {
  if (!apiKey) throw new Error('No API key');
  await throttleCheck();
  const res = await fetch('https://api.anthropic.com/v1/messages', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      'x-api-key': apiKey,
      'anthropic-version': '2023-06-01',
      'anthropic-dangerous-direct-browser-access': 'true'
    },
    body: JSON.stringify({
      model: 'claude-sonnet-4-20250514',
      max_tokens: maxTokens,
      system: getSystemExtraction(),
      messages
    })
  });

  if (res.status === 529 || res.status === 429) {
    const retryAfter = parseInt(res.headers?.get?.('retry-after') || '30');
    const wait = Math.min(retryAfter, 60) * 1000;
    $('progress-current').textContent = `Rate limited â€” waiting ${Math.round(wait/1000)}sâ€¦`;
    await sleep(wait);
    const res2 = await fetch('https://api.anthropic.com/v1/messages', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json', 'x-api-key': apiKey, 'anthropic-version': '2023-06-01', 'anthropic-dangerous-direct-browser-access': 'true' },
      body: JSON.stringify({ model: 'claude-sonnet-4-20250514', max_tokens: maxTokens, system: getSystemExtraction(), messages })
    });
    if (res2.status === 429 || res2.status === 529) {
      showPauseOverlay($('progress-current').textContent);
      await waitForResume();
      setBucketReset(Date.now()); setBucketUsed(0);
      return callExtractionClaude(messages, maxTokens);
    }
    if (!res2.ok) { const e2 = await res2.json().catch(()=>({})); throw new Error(e2?.error?.message||`API ${res2.status}`); }
    const d2 = await res2.json();
    try {
      const d2raw = d2.content.map(b=>b.text||'').join('').trim()
        .replace(/^```json\n?|^```\n?|```$/gm,'').trim()
        .replace(/"value"\s*:\s*<\s*([\d.]+)/g, (_, n) => `"value": "<${n}"`)
        .replace(/"refLow"\s*:\s*<\s*([\d.]+)/g, () => `"refLow": null`)
        .replace(/"refHigh"\s*:\s*<\s*([\d.]+)/g, () => `"refHigh": null`);
      return JSON.parse(d2raw);
    } catch { return null; }
  }

  if (!res.ok) {
    const err = await res.json().catch(() => ({}));
    throw new Error(err?.error?.message || `API ${res.status}`);
  }
  const data = await res.json();
  if (data.usage && window.updateSessionCost) {
    window.updateSessionCost(data.usage.input_tokens || 0, data.usage.output_tokens || 0);
  }
  const raw = data.content.map(b => b.text || '').join('').trim();
  if (data.stop_reason === 'max_tokens') {
    console.warn(`Response truncated at ${maxTokens} tokens â€” retrying with higher limit`);
    if (maxTokens < 16000) {
      return callExtractionClaude(messages, Math.min(maxTokens * 2, 16000));
    }
    console.error('Response still truncating at 16k tokens â€” returning partial result');
    try {
      const partial = JSON.parse(raw.replace(/^```json\n?|^```\n?|```$/gm, '').trim());
      if (!partial.flags) partial.flags = [];
      partial.flags.push({ type: 'chunk_skipped', description: 'Response truncated at 16k tokens â€” some data may be missing.' });
      return partial;
    } catch {
      return { skip: false, visits: [], labs: [], pcr: [], flags: [{ type: 'chunk_skipped', description: 'Response truncated and could not be parsed.' }] };
    }
  }
  console.log('Extraction raw response (first 2000):', raw.slice(0, 2000));
  try {
    const cleaned = raw
      .replace(/^```json\n?|^```\n?|```$/gm, '')
      .trim()
      .replace(/"value"\s*:\s*<\s*([\d.]+)/g, (_, n) => `"value": "<${n}"`)
      .replace(/"refLow"\s*:\s*<\s*([\d.]+)/g, (_, n) => `"refLow": null`)
      .replace(/"refHigh"\s*:\s*<\s*([\d.]+)/g, (_, n) => `"refHigh": null`);
    return JSON.parse(cleaned);
  } catch(e) {
    console.warn('JSON parse failed:', e.message);
    console.warn('Raw response (full):', raw);
    return null;
  }
}

