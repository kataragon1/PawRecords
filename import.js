// â”€â”€ THROTTLE â”€â”€
async function throttleCheck() {
  const now = Date.now();
  if (now - bucketReset > 60000) { setBucketReset(now); setBucketUsed(0); }
  setBucketUsed(bucketUsed + TOKENS_PER_CALL);
  if (bucketUsed > BUCKET_LIMIT) {
    const waitMs = 62000 - (Date.now() - bucketReset);
    await showThrottleCountdown(Math.ceil(waitMs / 1000));
    setBucketReset(Date.now()); setBucketUsed(TOKENS_PER_CALL);
  }
}

async function showThrottleCountdown(secs) {
  for (let i = secs; i > 0; i--) {
    $('progress-current').textContent = `Rate limit â€” cooling down ${i}sâ€¦`;
    await sleep(1000);
  }
}

